class Solution {
public:
    int hammingWeight(uint32_t n) {
        unsigned tmp = (1 << 31);
        int res = 0;
        for (int i = 0; i < 32; ++i) {
            if (n >= tmp) {
                res += 1;
                n -= tmp;
            }
            tmp = tmp >> 1;
        }
        return res;
    }
};

